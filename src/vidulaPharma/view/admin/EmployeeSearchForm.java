    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if(!nameText.getText().isEmpty() && !addressText.getText().isEmpty() && Validate.validateTelephone(telephoneText.getText())){
            Employee editedEmployee = new Employee(employee.getEmployeeId(), nameText.getText(), addressText.getText(), telephoneText.getText());
            boolean isUpdated=false;
            try {
                isUpdated = EmployeeController.editEmployee(editedEmployee);
            } catch (SQLException ex) {
                JOptionPane.showInternalMessageDialog(EmployeeSearchForm.this, "ERROR : UPDATE FAILED", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            if(isUpdated){
                JOptionPane.showInternalMessageDialog(EmployeeSearchForm.this, "Employee update Succcessfull","Success",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showInternalMessageDialog(EmployeeSearchForm.this, "ERROR : FILL ALL FIELDS CORRECTLY", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

