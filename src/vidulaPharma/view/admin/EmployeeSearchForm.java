    private void setListData() {
        ArrayList<Employee> employees = null;
        try {
            employees = (ArrayList<Employee>) EmployeeController.searchEmployee(searchText.getText(), "name");
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeSearchForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultListModel listModel = new DefaultListModel();
        for (Employee sup : employees) {
            listModel.addElement(sup);
        }
        employeeList.setModel(listModel);
    }
