    private void receiveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receiveButtonActionPerformed
        try {
            SupplierOrder sOrder = orderList.get(jTable1.getSelectedRow());
            SupplierPayment payment = SupplierPaymentController.searchSupplierPayment(sOrder.getSupplierPaymentId());
            ReceivePaymentForm receivePaymentForm = new ReceivePaymentForm(new JFrame(), true, ViewOrdersForm.this);
            receivePaymentForm.setVisible(true);
            if(!amount.isEmpty()){
                double dAmount = Double.parseDouble(amount);
                payment.setAmount(dAmount);
                payment.setIsPaid(true);
                boolean isUpdated = SupplierPaymentController.updatePayment(payment);
                if(isUpdated){
                    List<SupplierOrderDetail> orderDetails = SupplierOrderDetailController.getOrderDetails(sOrder.getSupplierOrderId());
                    ArrayList<Drug> drugList = new ArrayList<>();
                    for(SupplierOrderDetail sOrderDetail : orderDetails){
                        Drug drug = DrugController.searchDrugByTradeName(sOrderDetail.getDrugId());
                        drug.setQtyOnHand(sOrderDetail.getQty());
                        drug.setAvailability(true);
                        drugList.add(drug);
                    }
                    for(Drug drug : drugList){
                        boolean drugUpdated = DrugController.updateDrug(drug);
                        JOptionPane.showInternalMessageDialog(ViewOrdersForm.this, drugUpdated);
                    }
                    
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewOrdersForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_receiveButtonActionPerformed

