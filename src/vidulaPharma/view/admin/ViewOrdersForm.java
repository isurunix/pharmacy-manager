    private void dateChooserPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_dateChooserPropertyChange
        try {
            System.out.println(dateChooser.getDate().toString());
            SimpleDateFormat sdformat = new SimpleDateFormat("dd/MM/yyyy");
            date = sdformat.format(dateChooser.getDate());
            orderList = SupplierOrderController.searchByDate(date);
            setTable(orderList);
        } catch (SQLException ex) {
            Logger.getLogger(ViewOrdersForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_dateChooserPropertyChange

    private void setTable(List<SupplierOrder> orderList) {
        dtModel.setRowCount(0);
        for (SupplierOrder sOrder : orderList) {
            try {
                Supplier supplier = SupplierController.getSupplier(sOrder.getSupplierId());
                Object[] rowData = {sOrder.getSupplierOrderId(), supplier.getSupplierName(), sOrder.getSupplierOrderDate()};
                dtModel.addRow(rowData);
            } catch (SQLException ex) {
                Logger.getLogger(ViewOrdersForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        jTable1.setModel(dtModel);
    }
    private void detailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailsButtonActionPerformed
        try {
            SupplierOrder sOrder = orderList.get(jTable1.getSelectedRow());
            List<SupplierOrderDetail> orderDetails = SupplierOrderDetailController.getOrderDetails(sOrder.getSupplierOrderId());
            OrderDetailsForm odf = OrderDetailsForm.getInstance();
            odf.setTabel((ArrayList<SupplierOrderDetail>) orderDetails);
            AdminMainForm.getInstance().showForm(odf);
        } catch (SQLException ex) {
            Logger.getLogger(ViewOrdersForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_detailsButtonActionPerformed

