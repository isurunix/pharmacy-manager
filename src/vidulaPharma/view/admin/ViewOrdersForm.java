    private void setCombo() {
        try {
            List<Supplier> supList = SupplierController.getAll();
            for (Supplier supplier : supList) {
                supplierCombo.addItem(supplier);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewOrdersForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void supplierComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supplierComboActionPerformed
        if (supplierCombo.getSelectedIndex() > 0) {
            try {
                Supplier supplier = (Supplier) supplierCombo.getSelectedItem();
                orderList = SupplierOrderController.searchSupplierOrder(supplier.getSupplierId(), "orderId");
                setTable(orderList);
            } catch (SQLException ex) {
                Logger.getLogger(ViewOrdersForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_supplierComboActionPerformed

